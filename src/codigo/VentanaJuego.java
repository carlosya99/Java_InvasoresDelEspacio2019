    Disparo miDisparo = new Disparo();
    Marciano miMarciano = new Marciano();
    
    Timer temporizador = new Timer(10, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            bucleDelJuego();
        }
    });
    
